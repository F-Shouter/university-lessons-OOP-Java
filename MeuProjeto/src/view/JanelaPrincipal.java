            nomeTxt.setText("");
            descricaoTxt.setText("");
            precoTxt.setText("");
            transportadoraTxt.setText("");
            origemTxt.setText("");
            codigoTxt.setText("");
            materialTxt.setText("");
            proprietarioTxt.setText("");
            contatoTxt.setText("");
