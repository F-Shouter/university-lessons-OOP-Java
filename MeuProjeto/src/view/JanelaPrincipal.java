    private void consultarbyidBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarbyidBtnActionPerformed
        // TODO add your handling code here:
        mensagemLbl.setText("...");
        ProdutoDAO pdao = new ProdutoDAO();
        Produto p = new Produto();
        p.setId(Integer.parseInt(idTxt.getText()));

        try {
            boolean idExiste = pdao.verificarExistenciaID(p);
            if (idExiste) {
                p = pdao.consultarById(p);
                nomeTxt.setText(p.getNome());
                descricaoTxt.setText(p.getDescricao());
                precoTxt.setText(Double.toString(p.getPreco()));
                transportadoraTxt.setText(p.getTransportadora());
                origemTxt.setText(p.getOrigem());
                codigoTxt.setText(p.getCodigo());
                materialTxt.setText(p.getMaterial());
                proprietarioTxt.setText(p.getProprietario());
                contatoTxt.setText(p.getContato());

                mensagemLbl.setText("<html><strong>Produto encontrado!</strong><br>"
                        + "<br><strong>ID:</strong> " + p.getId()
                        + "<br><strong>Nome:</strong> " + p.getNome()
                        + "<br><strong>Descrição:</strong> " + p.getDescricao()
                        + "<br><strong>Preço:</strong> " + p.getPreco()
                        + "<br><strong>Transportadora:</strong> " + p.getTransportadora()
                        + "<br><strong>Origem:</strong> " + p.getOrigem()
                        + "<br><strong>Código:</strong> " + p.getCodigo()
                        + "<br><strong>Material:</strong> " + p.getMaterial()
                        + "<br><strong>Proprietário:</strong> " + p.getProprietario()
                        + "<br><strong>Contato:</strong> " + p.getContato()
                        + "</html>");
            } else {
                mensagemLbl.setText("<html><strong>Produto não encontrado</strong>" + "<br><strong>com o ID fornecido. </strong>" + "</html>");
                idTxt.setText("");
                nomeTxt.setText("");
                descricaoTxt.setText("");
                precoTxt.setText("");
                transportadoraTxt.setText("");
                origemTxt.setText("");
                codigoTxt.setText("");
                materialTxt.setText("");
                proprietarioTxt.setText("");
                contatoTxt.setText("");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_consultarbyidBtnActionPerformed

