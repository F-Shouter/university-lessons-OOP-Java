    private void atualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarBtnActionPerformed
        // TODO add your handling code here:
        mensagemLbl.setText("...");
        ProdutoDAO pdao = new ProdutoDAO();
        Produto p = new Produto();
        p.setId((int) Double.parseDouble(idTxt.getText()));
        p.setNome(nomeTxt.getText());
        p.setDescricao(descricaoTxt.getText());
        p.setPreco(Double.parseDouble(precoTxt.getText()));
        p.setTransportadora(transportadoraTxt.getText());
        p.setOrigem(origemTxt.getText());
        p.setCodigo(codigoTxt.getText());
        p.setMaterial(materialTxt.getText());
        p.setProprietario(proprietarioTxt.getText());
        p.setContato(contatoTxt.getText());

        try {
            pdao.atualizar(p);
            mensagemLbl.setText("<html><strong>Atualizado com sucesso!</strong></html>");
            nomeTxt.setText("");
            descricaoTxt.setText("");
            precoTxt.setText("");
            transportadoraTxt.setText("");
            origemTxt.setText("");
            codigoTxt.setText("");
            materialTxt.setText("");
            proprietarioTxt.setText("");
            contatoTxt.setText("");
        } catch (ClassNotFoundException | SQLException ex) {
            mensagemLbl.setText("ERRO: " + ex.getMessage());
        }

    }//GEN-LAST:event_atualizarBtnActionPerformed

    private void deletarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarBtnActionPerformed
        // TODO add your handling code here:
        mensagemLbl.setText("...");
        ProdutoDAO pdao = new ProdutoDAO();
        Produto p = new Produto();
        p.setId(Integer.parseInt(idTxt.getText()));

        try {
            boolean idExiste = pdao.verificarExistenciaID(p);
            if (idExiste) {
                // Se o ID existe, exibir a caixa de diálogo de confirmação
                int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja deletar?", "Confirmação", JOptionPane.YES_NO_OPTION);

                if (confirmacao == JOptionPane.YES_OPTION) {
                    // Se o usuário confirmar, então tenta deletar
                    boolean deletado = pdao.deletar(p);
                    if (deletado) {
                        mensagemLbl.setText("<html><strong>Deletado com sucesso.</strong></html>");
                        idTxt.setText("");
                        nomeTxt.setText("");
                        descricaoTxt.setText("");
                        precoTxt.setText("");
                        transportadoraTxt.setText("");
                        origemTxt.setText("");
                        codigoTxt.setText("");
                        materialTxt.setText("");
                        proprietarioTxt.setText("");
                        contatoTxt.setText("");
                    }

                } else {
                    idTxt.setText("");
                    nomeTxt.setText("");
                    descricaoTxt.setText("");
                    precoTxt.setText("");
                    transportadoraTxt.setText("");
                    origemTxt.setText("");
                    codigoTxt.setText("");
                    materialTxt.setText("");
                    proprietarioTxt.setText("");
                    contatoTxt.setText("");
                    mensagemLbl.setText("");
                }
            } else {
                // Se o ID não existe, exibir mensagem de erro
                mensagemLbl.setText("<html><strong>Nenhum ID encontrado.</strong></html>");
                idTxt.setText("");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            mensagemLbl.setText("ERRO: " + ex.getMessage());
        }
    }//GEN-LAST:event_deletarBtnActionPerformed

