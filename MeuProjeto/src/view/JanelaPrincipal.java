    private void consultartodosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultartodosBtnActionPerformed
        // TODO add your handling code here:
        mensagemLbl.setText("...");
        idTxt.setText("");
        ProdutoDAO pdao = new ProdutoDAO();

        try {
            List<Produto> produtos = pdao.consultarTodos();
            if (produtos.isEmpty()) {
                mensagemLbl.setText("<html><strong>Sem nenhum produto cadastrado.</strong></html>");
            } else {

                StringBuilder mensagem = new StringBuilder("<html>");
                for (Produto p : produtos) {
                    mensagem.append("<br><strong>ID:</strong> ").append(p.getId())
                            .append("<br><strong>Nome:</strong> ").append(p.getNome())
                            .append("<br><strong>Descrição:</strong> ").append(p.getDescricao())
                            .append("<br><strong>Preço:</strong> ").append(p.getPreco())
                            .append("<br><strong>Transportadora:</strong> ").append(p.getTransportadora())
                            .append("<br><strong>Origem:</strong> ").append(p.getOrigem())
                            .append("<br><strong>Código:</strong> ").append(p.getCodigo())
                            .append("<br><strong>Material:</strong> ").append(p.getMaterial())
                            .append("<br><strong>Proprietário:</strong> ").append(p.getProprietario())
                            .append("<br><strong>Contato:</strong> ").append(p.getContato())
                            .append("<br>---------------------------")
                            .append("<br>");
                }
                mensagem.append("</html>");

                mensagemLbl.setText(mensagem.toString());
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_consultartodosBtnActionPerformed

